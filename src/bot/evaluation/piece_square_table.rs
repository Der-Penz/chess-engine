#[inline(always)]
pub fn read_p_sq_table(piece: Piece, sq: u8) -> i64 {
    let index = match piece.color() {
        crate::game::Color::White => {
            let file = sq % 8;
            let rank = 7 - (sq / 8);

            file + rank * 8
        }
        crate::game::Color::Black => sq,
    } as usize;

    PIECE_SQUARE_TABLES[piece.ptype() as usize][index] as i64
}
